	</p>
	<label><strong>Ideario</strong></label>
	<p>
		La unidad Educativa Fiscal “PATRIA”, considera  a los niños, niñas y adolescentes como el centro del quehacer educativo, todas las acciones y actividades están orientadas a generar aprendizajes significativos; garantizando la participación de sus estudiantes en eventos  sociales, culturales, científicos y deportivos, que le permitan alcanzar el desarrollo integral de la persona, formando entes constructivistas con las herramientas necesarias, para vivir en armonía con la sociedad y la naturaleza. 
		<br>
		<br>
