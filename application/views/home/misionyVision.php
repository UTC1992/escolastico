    <center><h2>Misión y Visión</h2></center>
	<label><strong>Misión</strong></label>
	<p>
		La unidad Educativa Fiscal “PATRIA” de la ciudad de Latacunga, imparte una educación inclusiva, intercultural y con equidad de género, orientada al desarrollo de destrezas, valores y pensamiento crítico, en los niveles: inicial, básica y bachillerato general unificado, formando estudiantes autónomos, responsables y solidarios, preparados para destacar por sus propios méritos en un mundo de tecnología cambiante, en el cual puedan convivir con respeto y tolerancia.
